// while loop

let x = 0;

while(x <= 200) {
    console.log('X è uguale a '+ x);
    x = x +50;
}

// for loop

for(let x = 0; x <= 200; x = x + 50) {
    console.log('X è uguale a '+ x);
}

const fruits = ['banana','apple','peach'];

const index = 1;

console.log(fruits[index]);

// increment

// crescente
for (let i = 0; i < fruits.length; i++) {
    console.log(fruits[i])

}
// decrescente
for (let i = fruits.length - 1; i >= 0; i--) {
    console.log(fruits[i])

}


